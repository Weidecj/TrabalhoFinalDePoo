    public void addTrabalhoFinalDePoo(TrabalhoFinalDePoo t){
        this.array.add(t);
        this.addDadosNaTabela(t);
    }
    
    public void addDadosNaTabela (TrabalhoFinalDePoo t){
        DefaultTableModel model = (DefaultTableModel)jTable1.getModel();
        SimpleDateFormat tt = new SimpleDateFormat("dd/MM/yyyy"); 
        String data = tt.format(t.getDataDeNascimento());
                
        model.addRow(new Object[]{t.getNome(), t.getCpf(), t.getTipoSanguineo(),data,t.getEndereco(),t.getCidade(), t.getUF(), t.getEmail(), t.getTelefone(), t.getCelular()});
         this.array.add(t);
    }
    
    public void removeDados(int i){
        array.remove(i);
        removeDadosDaTabela(i);
    }
    
    public void removeDadosDaTabela(int i){
        DefaultTableModel model = (DefaultTableModel)jTable1.getModel();
        model.removeRow(i);
    }
    
    public void redesenhaTabela(){
        DefaultTableModel model = (DefaultTableModel)jTable1.getModel();
        while (model.getRowCount() > 0){
            model.removeRow(0);
        }
        for (TrabalhoFinalDePoo t: array){
            SimpleDateFormat tt = new SimpleDateFormat("dd/MM/yyyy"); 
            String data = tt.format(t.getDataDeNascimento());
            model.addRow(new Object[]{t.getNome(), t.getCpf(), t.getTipoSanguineo(),data,t.getEndereco(),t.getCidade(), t.getUF(), t.getEmail(), t.getTelefone(), t.getCelular()});
        }
    }
    
